public class Arithmetic {
    int num1, num2;
    public Arithmetic(int num1, int num2)
    {
        this.num1 = num1;
        this.num2 = num2;
    }
    public int sum(){
        return num1+num2;
    }

    public int diff(){
        return num1-num2;
    }

    public int product(){
        return num1*num2;
    }

    public int divison(){
        return num1/num2;
    }





}
